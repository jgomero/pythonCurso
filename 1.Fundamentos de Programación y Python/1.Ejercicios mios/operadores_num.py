#Operadores numericos
a = 10
b = 10.0

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicacion:", a * b)
print("Potenciacion:", a ** b)
print("Division:", a / b)
print("Parte Entera Division:", a // b)
print("Modulo:", a % b) #Es el residuo

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)